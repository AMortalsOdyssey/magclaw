function translateEnglishText(value) {
  if (currentMagclawLanguage !== 'zh-CN') return String(value ?? '');
  const raw = String(value ?? '');
  const leading = raw.match(/^\s*/)?.[0] || '';
  const trailing = raw.match(/\s*$/)?.[0] || '';
  const text = raw.trim().replace(/\s+/g, ' ');
  if (!text) return raw;
  const direct = zhCnTranslations[text];
  if (direct) return `${leading}${direct}${trailing}`;
  for (const [pattern, replacement] of zhCnRules) {
    if (pattern.test(text)) return `${leading}${text.replace(pattern, replacement)}${trailing}`;
  }
  return raw;
}

function t(value) {
  return translateEnglishText(value);
}

function shouldSkipTranslationNode(node) {
  const element = node.nodeType === Node.ELEMENT_NODE ? node : node.parentElement;
  if (!element) return true;
  const tag = element.tagName;
  if (['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE', 'TEXTAREA'].includes(tag)) return true;
  return Boolean(element.closest?.('[data-i18n-skip], .message-markdown, .saved-row-text, .project-chip-path, .project-tree-name, .workspace-activity-detail'));
}

function translateElementAttributes(element) {
  for (const attr of ['placeholder', 'title', 'aria-label', 'data-tooltip', 'alt']) {
    if (!element.hasAttribute?.(attr)) continue;
    const current = element.getAttribute(attr);
    const translated = translateEnglishText(current);
    if (translated !== current) element.setAttribute(attr, translated);
  }
}

function translatePage(scope = document.body) {
  if (!scope || magclawTranslating || currentMagclawLanguage !== 'zh-CN') return;
  magclawTranslating = true;
  try {
    document.documentElement.lang = 'zh-CN';
    if (document.title) document.title = translateEnglishText(document.title);
    const rootElement = scope.nodeType === Node.ELEMENT_NODE ? scope : document.body;
    if (rootElement && !shouldSkipTranslationNode(rootElement)) translateElementAttributes(rootElement);
    const walker = document.createTreeWalker(rootElement || document.body, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
    while (walker.nextNode()) {
      const node = walker.currentNode;
      if (node.nodeType === Node.TEXT_NODE) {
        if (shouldSkipTranslationNode(node)) continue;
        const translated = translateEnglishText(node.nodeValue);
        if (translated !== node.nodeValue) node.nodeValue = translated;
      } else if (node.nodeType === Node.ELEMENT_NODE) {
        if (!shouldSkipTranslationNode(node)) translateElementAttributes(node);
      }
    }
  } finally {
    magclawTranslating = false;
  }
}

function startMagclawTranslationObserver() {
  if (magclawTranslationObserver || typeof MutationObserver === 'undefined') return;
  magclawTranslationObserver = new MutationObserver(() => {
    if (magclawTranslating || currentMagclawLanguage !== 'zh-CN') return;
    window.queueMicrotask(() => translatePage(document.body));
  });
  magclawTranslationObserver.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'title', 'aria-label', 'data-tooltip', 'alt'],
  });
}

function setMagclawLanguage(language) {
  const next = normalizeMagclawLanguage(language);
  if (next === currentMagclawLanguage) return;
  currentMagclawLanguage = next;
  try {
    localStorage.setItem(MAGCLAW_LANGUAGE_KEY, next);
  } catch {
    // Keep the in-memory language if browser storage is unavailable.
  }
  document.documentElement.lang = next === 'zh-CN' ? 'zh-CN' : 'en';
  if (typeof render === 'function' && typeof appState !== 'undefined' && appState) {
    render();
  } else if (next === 'zh-CN') {
    translatePage(document.body);
  }
  if (typeof toast === 'function') toast(next === 'zh-CN' ? '界面语言已切换为中文' : 'Interface language set to English');
}

