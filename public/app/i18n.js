function normalizeMagclawLanguage(value) {
  const raw = String(value || '').trim().toLowerCase();
  if (raw === 'zh' || raw === 'zh-cn' || raw === 'cn' || raw === 'chinese') return 'zh-CN';
  if (raw === 'en' || raw === 'en-us' || raw === 'english') return 'en';
  return MAGCLAW_DEFAULT_LANGUAGE;
}

function readMagclawLanguage() {
  try {
    return normalizeMagclawLanguage(localStorage.getItem(MAGCLAW_LANGUAGE_KEY) || MAGCLAW_DEFAULT_LANGUAGE);
  } catch {
    return MAGCLAW_DEFAULT_LANGUAGE;
  }
}

